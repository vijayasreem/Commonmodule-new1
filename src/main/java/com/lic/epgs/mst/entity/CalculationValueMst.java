package com.lic.epgs.mst.entity;

import java.io.Serializable;
import java.sql.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name = "MASTER_CALCULATION_VALUE")
public class CalculationValueMst implements Serializable {

	private static final long serialVersionUID = 2064263195138765828L;

	@Id
	@GeneratedValue(strategy = GenerationType.AUTO)
	@Column(name = "CALCULATION_VALUE_ID")
	private long calculationValueId;

	@Column(name = "CALCULATION_VALUE_CODE")
	private String calculationValueCode;

	@Column(name = "DESCRIPTION")
	private String description;

	@Column(name = "CREATED_ON")
	private Date createdOn;

	@Column(name = "CREATED_BY")
	private String createdBy;

	public long getCalculationValueId() {
		return calculationValueId;
	}

	public void setCalculationValueId(long calculationValueId) {
		this.calculationValueId = calculationValueId;
	}

	public String getCalculationValueCode() {
		return calculationValueCode;
	}

	public void setCalculationValueCode(String calculationValueCode) {
		this.calculationValueCode = calculationValueCode;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public Date getCreatedOn() {
		return createdOn;
	}

	public void setCreatedOn(Date createdOn) {
		this.createdOn = createdOn;
	}

	public String getCreatedBy() {
		return createdBy;
	}

	public void setCreatedBy(String createdBy) {
		this.createdBy = createdBy;
	}

	public static long getSerialversionuid() {
		return serialVersionUID;
	}

}
