package com.lic.epgs.mst.modal;

public class StateCodeModel {

	private String stateCode;
	
	private String stateType;
	
	private String operatingUnitType;

	public String getStateCode() {
		return stateCode;
	}

	public void setStateCode(String stateCode) {
		this.stateCode = stateCode;
	}

	public String getStateType() {
		return stateType;
	}

	public void setStateType(String stateType) {
		this.stateType = stateType;
	}

	public String getOperatingUnitType() {
		return operatingUnitType;
	}

	public void setOperatingUnitType(String operatingUnitType) {
		this.operatingUnitType = operatingUnitType;
	}

	
	
}
